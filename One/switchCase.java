package One;

import java.util.*;

public class switchCase {
	public static void main(String []args)
 {
	 Scanner sc = new Scanner(System.in);
	 
	 String day = sc.nextLine();
	 
	 switch(day) {
	 
	 case "MONDAY":
		 System.out.println("You eat green Vegetable");
		 break;
		 
	 
 	case "TUESDAY":
	 System.out.println("You eat Yellow Vegetable");
	 break;
	 
 
	case "WEDNESDAY":
		System.out.println("You eat Red Vegetable");
		break;
		
		default :
			System.out.println(" Ya today is fast Day Fast");
			

}
	

}}
